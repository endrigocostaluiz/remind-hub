export function t(key, params = {}) {
    let text = locales[state.currentLang][key] || key;
    for (const param in params) {
        text = text.replace(`{${param}}`, params[param]);
    }
    return text;
}

export function updateUIForLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        el.innerHTML = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.dataset.i18nTitle;
        el.title = t(key);
    });

    const langDisplay = document.getElementById('currentLanguageDisplay');
    if (langDisplay) {
        langDisplay.innerText = state.currentLang === 'pt' ? 'Português (Brasil)' : 'English (US)';
    }

    updateAppVersion();
    updateSidebarLanguageToggle();
}

export function updateSidebarLanguageToggle() {
    const toggleText = document.getElementById('languageToggleText');
    if (toggleText) {
        toggleText.innerText = state.currentLang === 'pt' ? 'English (US)' : 'Português (Brasil)';
    }
}

export function updateAppVersion() {
    try {
        if (typeof chrome !== 'undefined' && chrome.runtime && chrome.runtime.getManifest) {
            const manifest = chrome.runtime.getManifest();
            const version = manifest.version;
            
            // Sidebar
            const versionEl = document.getElementById('appVersion');
            if (versionEl) {
                versionEl.innerText = `${t('version')} ${version}`;
            }

            // About Tab
            const aboutVersion = document.getElementById('aboutVersion');
            if (aboutVersion) {
                aboutVersion.innerText = `${t('version')} ${version}`;
            }
            
            // Modal Novidades
            const modalTitle = document.getElementById('whatsNewVersionTitle');
            if (modalTitle) {
                modalTitle.innerHTML = t('whatsNewTitle', { version: version });
            }

            // Página / Título
            document.title = `Remind.hub - ${t('version')} ${version}`;
        }
    } catch (e) {
    }
}
